/**
 * @module @conveyor/shared/types
 *
 * Core type definitions for Conveyor.
 * All packages depend on these types — they are the contract.
 */

/** Supported time unit suffixes for human-readable durations. */
export type TimeUnit =
  | 'ms'
  | 'millisecond'
  | 'milliseconds'
  | 's'
  | 'second'
  | 'seconds'
  | 'm'
  | 'minute'
  | 'minutes'
  | 'h'
  | 'hour'
  | 'hours'
  | 'd'
  | 'day'
  | 'days'
  | 'w'
  | 'week'
  | 'weeks';

/**
 * A human-readable duration string (e.g. `"5s"`, `"10 minutes"`, `"2h"`).
 *
 * Accepted formats: `"<number><unit>"` or `"<number> <unit>"`.
 */
export type HumanDuration = `${number}${TimeUnit}` | `${number} ${TimeUnit}`;

/**
 * A schedule expression — human duration with optional `"in"` prefix.
 *
 * Accepted formats: `"5s"`, `"10 minutes"`, `"in 10 minutes"`.
 */
export type ScheduleDelay = HumanDuration | `in ${HumanDuration}`;

/** A delay value — either milliseconds (number) or a human-readable string. */
export type Delay = number | HumanDuration;

/** Possible states of a job in its lifecycle. */
export type JobState =
  | 'waiting'
  | 'waiting-children'
  | 'delayed'
  | 'active'
  | 'completed'
  | 'failed';

/**
 * Record of a single processing attempt for a job.
 * One record is created each time a worker picks up the job.
 */
export interface AttemptRecord {
  /** 1-based attempt number. */
  attempt: number;
  /** When this attempt started processing (ISO 8601). */
  startedAt: string;
  /** When this attempt ended (ISO 8601), or `null` if still running. */
  endedAt: string | null;
  /** Outcome of this attempt. */
  status: 'completed' | 'failed';
  /** Error message if the attempt failed. */
  error: string | null;
  /** Stack trace if the attempt failed. */
  stacktrace: string | null;
  /** Logs produced during this specific attempt. */
  logs: string[];
}

/**
 * The raw data structure stored for each job.
 * This is the shape read from and written to the store.
 *
 * @typeParam T - The type of the job payload.
 */
export interface JobData<T = unknown> {
  /** Unique job ID (generated by the store or provided via jobId option). */
  id: string;

  /** Job name (e.g. `"send-email"`, `"process-image"`). */
  name: string;

  /** Queue this job belongs to. */
  queueName: string;

  /** Job payload — generic, typed by the consumer. */
  data: T;

  /** Current state in the lifecycle. */
  state: JobState;

  /** Number of processing attempts made. */
  attemptsMade: number;

  /** Progress percentage (0–100). */
  progress: number;

  /** Return value from successful processing. */
  returnvalue: unknown;

  /** Error message if failed. */
  failedReason: string | null;

  /** Job options (serialized). */
  opts: JobOptions;

  /** Deduplication key (computed hash or custom key). */
  deduplicationKey: string | null;

  /** Logs appended during processing. */
  logs: string[];

  /** Stack traces accumulated across retry attempts. */
  stacktrace: string[];

  /** When the job was created. */
  createdAt: Date;

  /** When the job started processing. */
  processedAt: Date | null;

  /** When the job completed successfully. */
  completedAt: Date | null;

  /** When the job failed. */
  failedAt: Date | null;

  /** When the delayed job should be promoted to waiting. */
  delayUntil: Date | null;

  /** Lock expiration timestamp (`null` = not locked). */
  lockUntil: Date | null;

  /** Worker ID holding the lock. */
  lockedBy: string | null;

  /** ID of parent job (`null` if standalone). */
  parentId: string | null;

  /** Queue name of parent job (for cross-queue flows). */
  parentQueueName: string | null;

  /** Number of children not yet completed (`0` for leaf/standalone jobs). */
  pendingChildrenCount: number;

  /** When the job was cancelled. */
  cancelledAt: Date | null;

  /** Group ID this job belongs to (`null` if ungrouped). */
  groupId: string | null;

  /** Whether this job has been discarded (no more retries). */
  discarded: boolean;

  /** Per-attempt processing history. */
  attemptLogs: AttemptRecord[];
}

/** Configuration for retry backoff strategies. */
export interface BackoffOptions {
  /** The backoff strategy to use. */
  type: 'fixed' | 'exponential' | 'custom';

  /** Base delay in milliseconds. */
  delay: number;

  /** Custom backoff function, called with the current attempt number. */
  customStrategy?: (attemptsMade: number) => number;
}

/** Configuration for recurring (repeat) jobs. */
export interface RepeatOptions {
  /** Cron expression (5, 6, or 7 fields). */
  cron?: string;

  /** Interval in ms or human-readable (`"5 minutes"`, `"2 hours"`). */
  every?: Delay;

  /** Max number of repetitions. */
  limit?: number;

  /** When to start the first repeat. */
  startDate?: Date;

  /** When to stop repeating. */
  endDate?: Date;

  /** IANA timezone string (e.g. `"America/New_York"`). */
  tz?: string;
}

/** Configuration for job deduplication. */
export interface DeduplicationOptions {
  /** Hash the payload automatically for dedup. */
  hash?: boolean;

  /** Custom dedup key string. */
  key?: string;

  /** TTL for dedup entry in ms. After expiry, a new job can be created. */
  ttl?: number;
}

/** Options passed when adding a job to a queue. */
export interface JobOptions {
  /** Number of attempts before marking as failed (default: 1). */
  attempts?: number;

  /** Backoff strategy for retries. */
  backoff?: BackoffOptions;

  /** Delay before execution in ms or human-readable (`"5 minutes"`). */
  delay?: Delay;

  /** Repeat scheduling configuration. */
  repeat?: RepeatOptions;

  /** Lower number = higher priority (default: 0). */
  priority?: number;

  /** LIFO mode: last added = first processed (default: false). */
  lifo?: boolean;

  /** Deduplication configuration. */
  deduplication?: DeduplicationOptions;

  /** Remove job on completion: `true`, `false`, or max age in ms. */
  removeOnComplete?: boolean | number;

  /** Remove job on failure: `true`, `false`, or max age in ms. */
  removeOnFail?: boolean | number;

  /** Timeout in ms — job marked failed if exceeded. */
  timeout?: number;

  /** Custom job ID (manual dedup). */
  jobId?: string;

  /**
   * Policy when a child job fails.
   * - `'fail'` (default): parent fails immediately when any child fails
   * - `'ignore'`: parent proceeds when remaining children finish (failed ones skipped)
   * - `'remove'`: parent is removed if any child fails
   */
  failParentOnChildFailure?: 'fail' | 'ignore' | 'remove';

  /** Assign this job to a group. */
  group?: GroupOptions;
}

/** Options for assigning a job to a group. */
export interface GroupOptions {
  /** Group identifier (e.g. tenant ID, user ID). */
  id: string;

  /** Maximum number of waiting jobs allowed in this group. Throws on `add()` if exceeded. */
  maxSize?: number;
}

/** Worker-side group configuration. */
export interface GroupWorkerOptions {
  /** Max concurrent active jobs per group (distributed, enforced by the store). */
  concurrency?: number;

  /** Per-group rate limiter (worker-local sliding window). */
  limiter?: LimiterOptions;
}

/** Options for creating a {@linkcode Queue}. */
export interface QueueOptions {
  /** The store backend to use. */
  store: StoreInterface;

  /** Default options applied to all jobs added to this queue. */
  defaultJobOptions?: Partial<JobOptions>;
}

/** Configuration for batch processing. */
export interface BatchOptions {
  /** Number of jobs to collect per batch. */
  size: number;
}

/** Result for a single job within a batch. */
export type BatchResult =
  | { status: 'completed'; value?: unknown }
  | { status: 'failed'; error: Error };

/** Rate limiter configuration for workers. */
export interface LimiterOptions {
  /** Max jobs in the duration window. */
  max: number;

  /** Duration window in ms. */
  duration: number;
}

/** Options for creating a {@linkcode Worker}. */
export interface WorkerOptions {
  /** The store backend to use. */
  store: StoreInterface;

  /** Max concurrent jobs on this worker (default: 1). */
  concurrency?: number;

  /** Max concurrent active jobs across ALL workers (optional). */
  maxGlobalConcurrency?: number;

  /** Rate limiter configuration. */
  limiter?: LimiterOptions;

  /** Lock duration in ms (default: 30_000). */
  lockDuration?: number;

  /** Stalled check interval in ms (default: 30_000). */
  stalledInterval?: number;

  /** Start polling immediately on construction (default: true). Set to false to call start() manually. */
  autoStart?: boolean;

  /** LIFO mode: fetch most recently added job first (default: false). */
  lifo?: boolean;

  /** Batch processing configuration. When set, the worker fetches multiple jobs per cycle. */
  batch?: BatchOptions;

  /** Per-group concurrency and rate limiting configuration. */
  group?: GroupWorkerOptions;
}

/** Event types emitted by the store (used for cross-process pub/sub). */
export type StoreEventType =
  | 'job:waiting'
  | 'job:waiting-children'
  | 'job:active'
  | 'job:completed'
  | 'job:failed'
  | 'job:progress'
  | 'job:stalled'
  | 'job:delayed'
  | 'job:removed'
  | 'queue:drained'
  | 'queue:paused'
  | 'queue:resumed'
  | 'job:cancelled'
  | 'queue:error';

/** An event published through the store's pub/sub mechanism. */
export interface StoreEvent {
  /** The event type. */
  type: StoreEventType;

  /** Which queue this event belongs to. */
  queueName: string;

  /** The job ID related to this event, if any. */
  jobId?: string;

  /** Optional extra data attached to the event. */
  data?: unknown;

  /** When the event occurred. */
  timestamp: Date;
}

